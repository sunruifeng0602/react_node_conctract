module.exports = (err,ctx) =>{
    let status = 500
    switch (err.code){
        case '10001':
            status = 400
            break
        case '10002':
            status = 409
            break
        default :
            status = 500
    }
    ctx.status = status
    //console.log(err)
    ctx.err = err
    ctx.body = err
}